-- 출처: 각 단과대학 공식 홈페이지 (2026-07 기준)
--   eng.khu.ac.kr · eni.khu.ac.kr · software.khu.ac.kr · applsci.khu.ac.kr
--   cls.khu.ac.kr · kic.khu.ac.kr · foreign.khu.ac.kr · and.khu.ac.kr · sports.khu.ac.kr
--
-- 검증: 수집 결과의 '학과' 수 = 42개로 공식 2026 모집요강(국제캠퍼스 42개 학과)과 일치.
--   ※ '학부'는 4개(기계공학부·전자공학부·컴퓨터공학부·글로벌커뮤니케이션학부)로
--     모집요강 표기(3개)와 1개 차이가 있다. 모집요강은 조직 단위가 아니라
--     '모집단위' 기준이라 발생하는 차이로 보이며, 조직 기준인 공식 홈페이지를 따랐다.
--
-- ⚠️ 학과만 추가하고 course_catalog / curriculum_requirements는 넣지 않았다.
--    소프트웨어융합학과 외 학과는 /curriculum이 비어 있다(SPEC §2.1 참조).
--    교육과정 데이터는 학과별 공식 교육과정표가 필요하며, 틀린 졸업요건은
--    학생의 졸업을 실제로 막으므로 확인 없이 채우지 않는다.
--
-- 재실행 안전(idempotent). Supabase Dashboard → SQL Editor 에서 실행.
-- ════════════════════════════════════════════════════════════════════════

-- ── 중복 방지 제약 (재실행 안전의 전제) ──────────────────────────────────
-- 기존 스키마에 유니크 제약이 없어 이 시드를 두 번 돌리면 학과가 중복 생성된다.
DO $$ BEGIN
  ALTER TABLE colleges ADD CONSTRAINT colleges_name_campus_key UNIQUE (name, campus);
EXCEPTION WHEN duplicate_table OR duplicate_object THEN NULL; END $$;

DO $$ BEGIN
  ALTER TABLE departments ADD CONSTRAINT departments_college_name_key UNIQUE (college_id, name);
EXCEPTION WHEN duplicate_table OR duplicate_object THEN NULL; END $$;

